#!/usr/bin/env node
/**
 * retro-renderer.js
 * retro.jsonl + retro-patterns.json → retrospective-history.md 자동 렌더링
 */
'use strict';

const fs   = require('fs');
const path = require('path');

function render(logsDir) {
  // retro.jsonl 읽기
  const jsonlPath = path.join(logsDir, 'retro.jsonl');
  if (!fs.existsSync(jsonlPath)) return;
  const records = fs.readFileSync(jsonlPath, 'utf8').trim().split('\n')
    .filter(Boolean).map(l => { try { return JSON.parse(l); } catch { return null; } })
    .filter(Boolean);

  // retro-patterns.json 읽기
  let patterns = [];
  const patternsPath = path.join(logsDir, 'retro-patterns.json');
  if (fs.existsSync(patternsPath)) {
    try { patterns = JSON.parse(fs.readFileSync(patternsPath, 'utf8')); } catch {}
  }

  // markdown 생성
  const lines = [];
  lines.push('<!-- AUTO-GENERATED by retro-renderer.js — 직접 편집 금지. 원본: retro.jsonl -->');
  lines.push('# Maestro 회고 로그\n');

  if (patterns.length) {
    lines.push('## 반복 패턴');
    for (const p of patterns) {
      lines.push(`- **${p.name}**: ${p.count}회 / 마지막: ${p.last} / 개선: ${p.fix}`);
    }
    lines.push('');
  }

  // 역순 출력 (최신이 위)
  for (const r of [...records].reverse()) {
    lines.push('---\n');
    lines.push(`## ${r.date} — ${r.title} (${r.type}: ${r.pipeline})\n`);
    lines.push('| 항목 | 내용 |');
    lines.push('|------|------|');
    lines.push(`| 실행 | ${r.executed || ''} |`);
    lines.push(`| 건너뜀 | ${r.skipped || '없음'} |`);
    lines.push(`| 반복 이슈 | ${r.repeatIssue || '없음'} |`);
    lines.push('');
    lines.push(`**자기비평**: ${r.selfCritique || ''}`);
    lines.push(`**다음 번 개선**: ${r.nextImprovement || ''}`);
    lines.push('');
  }

  const outPath = path.join(logsDir, 'retrospective-history.md');
  const tmpPath = outPath + '.tmp';
  try {
    fs.writeFileSync(tmpPath, lines.join('\n'), 'utf8');
    fs.renameSync(tmpPath, outPath);
  } catch (e) {
    fs.writeFileSync(outPath, lines.join('\n'), 'utf8');
    try { fs.unlinkSync(tmpPath); } catch (_) {}
  }
}

module.exports = { render };

// CLI 직접 실행 시
if (require.main === module) {
  const logsDir = path.resolve(__dirname, '../../logs');
  render(logsDir);
  console.log('retrospective-history.md 렌더링 완료');
}
